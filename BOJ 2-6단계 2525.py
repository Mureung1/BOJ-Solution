H, M = map(int, input().split())
cooking_time = int(input())

# 1. 현재 분에 요리 시간을 모두 더한다
M += cooking_time

# 2. 늘어난 분(M)을 60으로 나눈 '몫'을 시(H)에 더한다
# (예: M이 140분이 되었다면 140 // 60 = 2. 즉, 2시간을 H에 더한다)
H += (M // 60)

# 3. M은 60으로 나눈 '나머지'가 된다
# (예: M이 140분이었다면 140 % 60 = 20. 즉, 최종 분은 20분이 된다)
M = M % 60

# 4. H가 24시 이상이 될 수 있으므로, H를 24로 나눈 '나머지'가 최종 시간이 된다
# (예: H가 25시가 되었다면 25 % 24 = 1. 즉, 새벽 1시가 된다)
H = H % 24

print(H, M)